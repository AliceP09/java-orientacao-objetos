package org.example;

public class MediaNota {
    public static void main(String[] args) {

        // Crie um programa que realize a média de duas notas decimais e exiba o resultado.

        double primeiraNota = 5.0;
        double segundaNota = 7.5;

        double resultadoMedia = (primeiraNota + segundaNota) / 2;

        System.out.println("Resultado da média entre a primeira nota: " + primeiraNota + " e a segunda nota: " + segundaNota + " é "
                + resultadoMedia );
    }
}